# comparison operators
a = 10
b = 5
print("a == b =", a == b)   # equal
print("a != b =", a != b)   # not equal
print("a > b =", a > b)     # greater than
print("a < b =", a < b)     # less than
print("a >= b =", a >= b)   # greater than or equal
print("a <= b =", a <= b)   # less than or equal

# comparison operators with strings
