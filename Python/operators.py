# arithmetic operators
a = 10
b = 5
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)       
print("a / b =", a / b)      # float division
print("a // b =", a // b)    # floor division
print("a % b =", a % b)      # modulus
print("a ** b =", a ** b)    # power

# assignment operators
a = 23

# compound assignment operators
a += 5          # a = a + 5
print("a =", a)

